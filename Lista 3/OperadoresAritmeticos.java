public class OperadoresAritmeticos {
	
	public static void main (String[] args) {
		
		int a = 10 + 1; //11
		int b = 10 - 2; //8
		int c = 20 * 3; //60
		int d = 25 / 5; //5
		int e = 10 % 4; //2
		
		System.out.println(a); 
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}

