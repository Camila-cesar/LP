public class PrePosIncrementoDecremento {
	
	public static void main (String[] args) {
		
		int a = 1;
		System.out.println( a ++ );		//1
		System.out.println( ++ a  );	//3
		System.out.println( a --); 		//3
		System.out.println( -- a ); 	//1
		
	}
}

